<?php
/**
 * Admin – Election Results Excel Export
 * Generates a full Excel (.xls) report with:
 *  - Election summary & winner box
 *  - Candidate vote breakdown
 *  - Individual voter details (who voted in each election)
 */
session_start();
include("../config/database.php");

if (!isset($_SESSION['admin'])) {
    header("Location: login.php");
    exit();
}

// Which election? Optional filter
$election_id = isset($_GET['election']) && is_numeric($_GET['election']) ? (int)$_GET['election'] : null;

// ── Fetch elections ──────────────────────────────────────────────────────────
if ($election_id) {
    $stmt = $conn->prepare("SELECT * FROM elections WHERE id = ?");
    $stmt->bind_param('i', $election_id);
    $stmt->execute();
    $elections = $stmt->get_result()->fetch_all(MYSQLI_ASSOC);
    $stmt->close();
    $filename  = "election_results_" . $election_id . "_" . date('Ymd_His') . ".xls";
} else {
    $elections = $conn->query("SELECT * FROM elections ORDER BY id DESC")->fetch_all(MYSQLI_ASSOC);
    $filename  = "all_election_results_" . date('Ymd_His') . ".xls";
}

// ── Headers to force Excel download ─────────────────────────────────────────
header("Content-Type: application/vnd.ms-excel; charset=UTF-8");
header("Content-Disposition: attachment; filename=\"$filename\"");
header("Pragma: no-cache");
header("Expires: 0");

// UTF-8 BOM for Excel to read Unicode correctly
echo "\xEF\xBB\xBF";
?>
<html xmlns:o="urn:schemas-microsoft-com:office:office"
      xmlns:x="urn:schemas-microsoft-com:office:excel"
      xmlns="http://www.w3.org/TR/REC-html40">
<head>
<meta charset="UTF-8">
<!--[if gte mso 9]><xml><x:ExcelWorkbook><x:ExcelWorksheets>
<?php $sheetNum = 0; foreach($elections as $elec): $sheetNum++; ?>
<x:ExcelWorksheet><x:Name><?php echo htmlspecialchars(substr($elec['title'],0,25)); ?></x:Name>
<x:WorksheetOptions><x:Selected/></x:WorksheetOptions></x:ExcelWorksheet>
<?php endforeach; ?>
</x:ExcelWorksheets></x:ExcelWorkbook></xml><![endif]-->
<style>
    body { font-family: Calibri, Arial, sans-serif; font-size: 11pt; }
    table { border-collapse: collapse; width: 100%; margin-bottom: 30px; }
    th {
        background-color: #4C1D95;
        color: #FFFFFF;
        font-weight: bold;
        padding: 8px 12px;
        border: 1px solid #6D28D9;
        text-align: left;
    }
    td { padding: 7px 12px; border: 1px solid #D1D5DB; vertical-align: middle; }
    tr:nth-child(even) td { background-color: #F5F3FF; }
    .section-header {
        background-color: #1E1040;
        color: #FFFFFF;
        font-size: 13pt;
        font-weight: bold;
        padding: 10px 14px;
    }
    .winner-box {
        background-color: #FEF3C7;
        border: 2px solid #F59E0B;
        font-weight: bold;
        color: #92400E;
    }
    .winner-row td { background-color: #FEF9E7 !important; font-weight: bold; }
    .label { font-weight: bold; color: #1E1040; }
    .meta-table td { border: none; padding: 4px 10px; }
    .meta-table { margin-bottom: 16px; }
    h1 { color: #1E1040; font-size: 16pt; margin: 0 0 4px 0; }
    h2 { color: #4C1D95; font-size: 13pt; margin: 20px 0 6px 0; border-bottom: 2px solid #7C3AED; padding-bottom: 4px; }
    h3 { color: #6D28D9; font-size: 11pt; margin: 14px 0 6px 0; }
    .report-title-row td {
        background: #1E1040;
        color: white;
        font-size: 16pt;
        font-weight: bold;
        padding: 14px 18px;
        border: none;
    }
    .generated-row td { background: #EDE9FE; color: #4C1D95; font-size: 9pt; padding: 5px 12px; border: none; }
</style>
</head>
<body>

<!-- ══════════════ REPORT HEADER ══════════════ -->
<table>
    <tr class="report-title-row">
        <td colspan="6">🗳️ VoteSecure – Election Results Report</td>
    </tr>
    <tr class="generated-row">
        <td colspan="6">
            Generated by: <?php echo htmlspecialchars($_SESSION['admin']); ?> &nbsp;|&nbsp;
            Date: <?php echo date('d M Y, H:i:s'); ?> &nbsp;|&nbsp;
            Total Elections: <?php echo count($elections); ?>
        </td>
    </tr>
</table>

<?php foreach ($elections as $election):
    $eid = (int)$election['id'];

    // ── Candidates with vote counts ─────────────────────────────────────────
    $cands = $conn->query(
        "SELECT c.id, c.name, c.position, COUNT(v.id) AS votes
         FROM candidates c
         LEFT JOIN votes v ON c.id = v.candidate_id
         WHERE c.election_id = $eid
         GROUP BY c.id
         ORDER BY votes DESC"
    )->fetch_all(MYSQLI_ASSOC);

    $total_votes = array_sum(array_column($cands, 'votes'));
    $winner      = !empty($cands) ? $cands[0] : null;
    $max_votes   = $winner ? (int)$winner['votes'] : 0;

    // ── Individual voters who voted in this election ────────────────────────
    $voters_in = $conn->query(
        "SELECT u.name, u.email, u.type, v.voted_at, c.name AS candidate_voted_for, c.position
         FROM votes v
         JOIN users u ON v.user_id = u.id
         JOIN candidates c ON v.candidate_id = c.id
         WHERE v.election_id = $eid
         ORDER BY v.voted_at ASC"
    )->fetch_all(MYSQLI_ASSOC);
?>

<!-- ══ ELECTION TITLE ══ -->
<table>
    <tr><td class="section-header" colspan="6">
        📋 <?php echo htmlspecialchars($election['title']); ?>
        — <?php echo htmlspecialchars($election['type']); ?>
        &nbsp;|&nbsp; Status: <?php echo htmlspecialchars($election['status']); ?>
    </td></tr>
</table>

<!-- ══ ELECTION META ══ -->
<table class="meta-table">
    <tr>
        <td class="label">Election ID:</td><td><?php echo $eid; ?></td>
        <td class="label">Type:</td><td><?php echo htmlspecialchars($election['type']); ?></td>
        <td class="label">Status:</td><td><?php echo htmlspecialchars($election['status']); ?></td>
    </tr>
    <tr>
        <td class="label">Start Date:</td><td><?php echo date('d M Y', strtotime($election['start_date'])); ?></td>
        <td class="label">End Date:</td><td><?php echo date('d M Y', strtotime($election['end_date'])); ?></td>
        <td class="label">Total Votes Cast:</td><td><strong><?php echo $total_votes; ?></strong></td>
    </tr>
</table>

<!-- ══ WINNER BOX ══ -->
<?php if ($winner && $max_votes > 0): ?>
<table>
    <tr>
        <td class="winner-box" colspan="6" style="text-align:center;font-size:12pt;padding:12px;">
            🏆 WINNER: <?php echo htmlspecialchars($winner['name']); ?>
            — <?php echo htmlspecialchars($winner['position']); ?>
            &nbsp;|&nbsp; Votes: <?php echo $winner['votes']; ?>
            (<?php echo ($total_votes > 0) ? round(($winner['votes']/$total_votes)*100,1) : 0; ?>%)
        </td>
    </tr>
</table>
<?php elseif (empty($cands)): ?>
<table>
    <tr><td colspan="6" style="color:#6b7280;padding:10px;font-style:italic;">No candidates in this election.</td></tr>
</table>
<?php else: ?>
<table>
    <tr><td colspan="6" style="color:#6b7280;padding:10px;font-style:italic;">No votes cast yet — winner TBD.</td></tr>
</table>
<?php endif; ?>

<br>

<!-- ══ CANDIDATE RESULTS TABLE ══ -->
<h3>📊 Candidate Vote Breakdown</h3>
<?php if (!empty($cands)): ?>
<table>
    <thead>
        <tr>
            <th>#</th>
            <th>Candidate Name</th>
            <th>Position</th>
            <th>Votes Received</th>
            <th>Vote Share (%)</th>
            <th>Remark</th>
        </tr>
    </thead>
    <tbody>
    <?php foreach ($cands as $i => $row):
        $pct    = ($total_votes > 0) ? round(($row['votes'] / $total_votes) * 100, 1) : 0;
        $is_win = ($max_votes > 0 && $row['votes'] == $max_votes);
    ?>
    <tr <?php echo $is_win ? 'class="winner-row"' : ''; ?>>
        <td><?php echo ($i+1); ?></td>
        <td><?php echo htmlspecialchars($row['name']); ?></td>
        <td><?php echo htmlspecialchars($row['position']); ?></td>
        <td><strong><?php echo $row['votes']; ?></strong></td>
        <td><?php echo $pct; ?>%</td>
        <td><?php echo $is_win ? '🏆 WINNER' : ''; ?></td>
    </tr>
    <?php endforeach; ?>
    <tr>
        <td colspan="3" style="text-align:right;font-weight:bold;">TOTAL</td>
        <td><strong><?php echo $total_votes; ?></strong></td>
        <td>100%</td>
        <td></td>
    </tr>
    </tbody>
</table>
<?php else: ?>
<table><tr><td colspan="6" style="color:#6b7280;font-style:italic;padding:10px;">No candidates found.</td></tr></table>
<?php endif; ?>

<br>

<!-- ══ WHO VOTED TABLE ══ -->
<h3>👥 Voters Who Participated (<?php echo count($voters_in); ?> voters)</h3>
<?php if (!empty($voters_in)): ?>
<table>
    <thead>
        <tr>
            <th>#</th>
            <th>Voter Name</th>
            <th>Email</th>
            <th>Voter Type</th>
            <th>Voted For</th>
            <th>Candidate Position</th>
            <th>Voted At</th>
        </tr>
    </thead>
    <tbody>
    <?php foreach ($voters_in as $j => $vrow): ?>
    <tr>
        <td><?php echo ($j+1); ?></td>
        <td><?php echo htmlspecialchars($vrow['name']); ?></td>
        <td><?php echo htmlspecialchars($vrow['email']); ?></td>
        <td><?php echo htmlspecialchars($vrow['type']); ?></td>
        <td><?php echo htmlspecialchars($vrow['candidate_voted_for']); ?></td>
        <td><?php echo htmlspecialchars($vrow['position']); ?></td>
        <td><?php echo date('d M Y, H:i', strtotime($vrow['voted_at'])); ?></td>
    </tr>
    <?php endforeach; ?>
    </tbody>
</table>
<?php else: ?>
<table><tr><td colspan="7" style="color:#6b7280;font-style:italic;padding:10px;">No votes recorded yet.</td></tr></table>
<?php endif; ?>

<br><br>

<?php endforeach; ?>

<!-- ══════════════ REPORT FOOTER ══════════════ -->
<table>
    <tr>
        <td colspan="6" style="background:#1E1040;color:#9CA3AF;font-size:9pt;padding:8px 14px;text-align:center;border:none;">
            VoteSecure – Confidential Election Report &nbsp;|&nbsp;
            Generated on <?php echo date('d M Y \a\t H:i:s'); ?> &nbsp;|&nbsp;
            Do not distribute without authorization.
        </td>
    </tr>
</table>

</body>
</html>
